package homework.day1.pretask;

public class IncrementDecrementExample {
    public static void main(String[] arg) {
        int a = 10;

        System.out.println(" a: " + a);

        System.out.println(" ++a: " + ++a);
        System.out.println(" After ++a " + a);

        System.out.println(" a++: " + a++);
        System.out.println(" After a++ " + a);

        System.out.println(" --a: " + --a);
        System.out.println(" After --a " + a);

        System.out.println(" a--: " + a--);
        System.out.println(" After a-- " + a);
    }
}
